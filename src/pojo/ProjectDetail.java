package pojo;

public class ProjectDetail {
	
	private String teamid,projecttitle,projectdesc,projecttechnology,projecttype,companyname,guidename,guidecontact,guideemail,status,creationdate;

	public String getTeamid() {
		return teamid;
	}

	public void setTeamid(String teamid) {
		this.teamid = teamid;
	}

	public String getProjecttitle() {
		return projecttitle;
	}

	public void setProjecttitle(String projecttitle) {
		this.projecttitle = projecttitle;
	}

	public String getProjectdesc() {
		return projectdesc;
	}

	public void setProjectdesc(String projectdesc) {
		this.projectdesc = projectdesc;
	}

	public String getProjecttechnology() {
		return projecttechnology;
	}

	public void setProjecttechnology(String projecttechnology) {
		this.projecttechnology = projecttechnology;
	}

	public String getProjecttype() {
		return projecttype;
	}

	public void setProjecttype(String projecttype) {
		this.projecttype = projecttype;
	}

	public String getCompanyname() {
		return companyname;
	}

	public void setCompanyname(String companyname) {
		this.companyname = companyname;
	}

	public String getGuidename() {
		return guidename;
	}

	public void setGuidename(String guidename) {
		this.guidename = guidename;
	}

	public String getGuidecontact() {
		return guidecontact;
	}

	public void setGuidecontact(String guidecontact) {
		this.guidecontact = guidecontact;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	
	public String getGuideemail() {
		return guideemail;
	}

	public void setGuideemail(String guideemail) {
		this.guideemail = guideemail;
	}

	public String getCreationdate() {
		return creationdate;
	}

	public void setCreationdate(String creationdate) {
		this.creationdate = creationdate;
	}
	
	
}
